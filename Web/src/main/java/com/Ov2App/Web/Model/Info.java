package com.Ov2App.Web.Model;

public class Info {

    public String inputA;
    public String inputB;

    public String test;
    public String Van;
    public String Naar;

    public String Voertuig;

    public String getInputA() {
        return inputA;
    }

    public void setInputA(String inputA) {
        this.inputA = inputA;
    }

    public String getInputB() {
        return inputB;
    }

    public void setInputB(String inputB) {
        this.inputB = inputB;
    }

    public String getTest() {
        return test;
    }

    public void setTest(String test) {
        this.test = test;
    }

    public String getVan() {
        return Van;
    }

    public void setVan(String van) {
        Van = van;
    }

    public String getNaar() {
        return Naar;
    }

    public void setNaar(String naar) {
        Naar = naar;
    }

    public String getVoertuig() {
        return Voertuig;
    }

    public void setVoertuig(String voertuig) {
        Voertuig = voertuig;
    }
}
